#Recorrer una lista con la estructura for
frutas=["manzana","fresa","uva"]
for elemento in frutas:
    print(elemento)
    
#Recorrer en un rango con la estructura for usando continue
   
for i in range(10):
    if i==6:
        continue
    print(i+1)
        
#Recorrer en un rango con la estructura for usando break
    
for i in range(10):
    if i==6:
        break
    print(i+1) 
    
